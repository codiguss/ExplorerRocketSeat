// OPERADORES DE COMPARAÇÃO

// Irá comprovar valores e retornar um Boolean como respota à comparação

let one = 1
let two = 2

// ==     igual a 
 console.log( two == 1 )
 console.log( one == "1" )

// !=   diferente de 
 console.log(one != two)
 console.log(one != 1)
 console.log(one != "1")

// ===  ESTRITAMENTE IGUAL A
 console.log( one === "1" )
 console.log( two === 1 )

// !==  ESTRITAMENTE DIFERENTE DE
 console.log( two !== "2")
 console.log( two !== 2 )

 // >  MAIOR QUE
 console.log(one > two)

 // >= MAIOR IGUAL A
 console.log(one < two)

 // <= Menor igual a
 console.log( one <= two)
 console.log( one <= 1)
 console.log( one <= 0)